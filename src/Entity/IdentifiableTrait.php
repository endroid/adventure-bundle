namespace Endroid\AdventureBundle\Entity;

trait IdentifiableTrait
{
    private $id;
    private $name;

    private function setIdentification(string $id, string $name)
    {
        $this->id = $id;
        $this->name = $name;
    }

    public function getId(): string
    {
        return $this->id;
    }

    public function getName(): string
    {
        return $this->name;
    }
}
